    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombres = txtNombre.getText().trim();
        String codigo = txtCodInterno.getText().trim();

        var modelArea = (DefaultComboBoxModel) cbArea.getModel();
        ListItem itemArea = (ListItem) modelArea.getSelectedItem();
        Long codArea = itemArea != null ? itemArea.getValor() : null;

        var filtros = new Trabajador();
        filtros.setCodigoTrabajador(codigo);
        filtros.setNombre(nombres);

        tblDatos.asignarDatos(TrabajadorService.getInstance().buscarPorFiltros(filtros));
    }//GEN-LAST:event_btnBuscarActionPerformed

