    protected void limpiar() {
        ((DefaultTableModel) tblDatos.getModel()).setRowCount(0);
        txtNombre.setText("");
        txtCodInterno.setText("");

        var modelEmpresa = (DefaultComboBoxModel) cbArea.getModel();
        modelEmpresa.setSelectedItem(null);
    }
