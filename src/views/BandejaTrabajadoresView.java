    public BandejaTrabajadoresView() {
        initComponents();

        // Especificamos como deberian mostrarse los datos en la tabla
        tblDatos.ejecutarAlCrearFila((item) -> new String[]{
            "", "",
            item.getCodigoTrabajador(),
            item.getNombre() + " " + item.getApellido(),
            item.getNumeroDocumento(),
            item.obtenerEdad() + "",
            item.getCodArea() != null ? AreaService.getInstance().obtenerSedePorCodArea(item.getCodArea()) : "",
            item.getCodArea() != null ? AreaService.getInstance().obtenerPorCodigo(item.getCodArea()).getNombre() : "",
            item.isActivo() ? "Activo" : "Inactivo"
        });

        // agregamos el boton de editar
        tblDatos.agregarBoton((item) -> {
            this.abrirRegistroTrabajador(item.getCodigoTrabajador());
        }, 0, "icons8-editar-20.png");

        tblDatos.agregarBoton((item) -> {
            int resp = -999;

            if (item.isActivo()) {
                resp = JOptionPane.showConfirmDialog(rootPane, "¿Desea inactivar el registro?", "Inactivar", JOptionPane.YES_NO_OPTION);
            } else {
                resp = JOptionPane.showConfirmDialog(rootPane, "¿Desea activar el registro?", "Activar", JOptionPane.YES_NO_OPTION);
            }

            if (resp == JOptionPane.YES_OPTION) {
                TrabajadorService.getInstance().cambiarEstado(item.getCodigoTrabajador());
                this.btnBuscarActionPerformed(null);
            }
        }, 1, (item) -> item.isActivo() ? "icons8-basura-llena-20.png" : "icons8-restart-20.png");

        tblDatos.asignarDatos(TrabajadorService.getInstance().listarTodosDatos());

        var modelArea = (DefaultComboBoxModel) cbArea.getModel();
        modelArea.removeAllElements();

        modelArea.addAll(AreaService.getInstance().listarAreaCombo());
    }

