    public RegistroTrabajadorView(String codTrabajador) {
        initComponents();
        var modelSede = (DefaultComboBoxModel) cbSede.getModel();

        var listaSedes = BdTemporal.getInstance().getListaSedes();
        for (String[] item : listaSedes) {
         modelSede.addElement(new ListItem2(item[0], item[1]));
        }

        if (codTrabajador != null) {
            lblId.setText("ID: " + codTrabajador);
            this.codTrabajador = codTrabajador;

            Trabajador trabajadorExistente = TrabajadorService.getInstance().obtenerPorCodigo(codTrabajador);
            txtApellido.setText(trabajadorExistente.getApellido());
            txtNombre.setText(trabajadorExistente.getNombre());
            txtDNI.setText(trabajadorExistente.getNumeroDocumento());
            txtFechaNacimiento.setLocalDate(trabajadorExistente.getFechaNacimiento());
            
            if (trabajadorExistente.getCodArea()!= null) {
                var area = AreaService.getInstance().obtenerPorCodigo(trabajadorExistente.getCodArea());
                String nombreSede = AreaService.getInstance().obtenerNombreSede(area.getCodSede());
                
                cbSede.setSelectedItem(new ListItem2(area.getCodSede(), nombreSede));
                cbArea.setSelectedItem(new ListItem(area.getCodArea(), area.getNombre()));
            }
        }
    }

