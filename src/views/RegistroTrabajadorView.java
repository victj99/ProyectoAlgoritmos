    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // ocultamos los textos de error
        this.ocultarTextosError();
        
        ListItem2 sedeSeleccionada = (ListItem2) cbSede.getSelectedItem();
        ListItem areaSeleccionada = (ListItem) cbArea.getSelectedItem();

        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String dni = txtDNI.getText().trim();

        if (sedeSeleccionada == null) {
            lblErrorEmpresa.setVisible(true);
            cbSede.setBorder(Constantes.ERROR_BORDER);
            return;
        }
        if (areaSeleccionada == null) {
            lblErrorArea.setVisible(true);
            cbArea.setBorder(Constantes.ERROR_BORDER);
            return;
        }
        
        if (StringUtils.isEmpty(nombre)) {
            lblErrorNombre.setVisible(true);
            txtNombre.setBorder(Constantes.ERROR_BORDER);
            return;
        }
        if (StringUtils.isEmpty(apellido)) {
            lblErrorApellido.setVisible(true);
            txtApellido.setBorder(Constantes.ERROR_BORDER);
            return;
        }
        if (StringUtils.isEmpty(dni)) {
            lblErrorDNI.setVisible(true);
            txtDNI.setBorder(Constantes.ERROR_BORDER);
            return;
        }

        int resp = JOptionPane.showConfirmDialog(rootPane, "¬øDesea registrar al trabajador?", "Registrar", JOptionPane.YES_NO_OPTION);

        if (resp != JOptionPane.YES_OPTION) {
            return;
        }

        var trabajador = new Trabajador();
        trabajador.setCodArea(areaSeleccionada.getValor());
        trabajador.setNombre(nombre);
        trabajador.setApellido(apellido);
        trabajador.setNumeroDocumento(dni);
        trabajador.setFechaNacimiento(txtFechaNacimiento.getLocalDate());
        trabajador.setActivo(true);

        if (this.codTrabajador == null) {
            TrabajadorService.getInstance().registrarDatos(trabajador);
        } else {
            trabajador.setCodigoTrabajador(codTrabajador);
            TrabajadorService.getInstance().actualizarDatos(trabajador);
        }

        JOptionPane.showMessageDialog(rootPane, "Registro completado", "Completado", JOptionPane.INFORMATION_MESSAGE);
        this.btnRegresarActionPerformed(null);
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void cbSedeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSedeItemStateChanged
        var modelArea = (DefaultComboBoxModel) cbArea.getModel();
        modelArea.removeAllElements();

        ListItem2 sede = (ListItem2) cbSede.getSelectedItem();

        if (sede == null) {
            return;
        }

        modelArea.addAll(AreaService.getInstance().listarAreasPorSedeCombo(sede.getValor()));

        cbArea.setEnabled(true);
    }//GEN-LAST:event_cbSedeItemStateChanged

